import math

# Get and print the log base 2 of 512.
log_512 = math.log(512, 2)
print("The log base 2 of 512 is", log_512)

# Now raise two back to that power and print.
raise2 = math.pow(2, log_512)
print("Two raised to the", log_512, "is", raise2)

# Take a square root of a perfect square and print.
root25 = math.sqrt(25)
print("The square root of 25 is", root25)
